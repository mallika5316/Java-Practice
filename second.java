public static void p8(int n) {
		int x=0,y=1;
		for(int i=0;i<n;i++) {
			
			for (int j = 0; j < n; j++) {  
                if (i - j >0) {  // Condition for triangular format
                    System.out.print(x + " ");
                    
                    // Update Fibonacci numbers
                    int next = x + y;
                    x = y;
                    y = next;
                } else {
                    System.out.print("  "); // Printing spaces for alignment
                }
            }
            System.out.println();
			}
		}






	
	
	
